public class swap {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int temp;
        System.out.println("values before swapping are  a ="+ a+ " b = "+ b);
        temp = a;
        a = b;
        b = temp;
System.out.println("values after swapping are  b ="+ a+ " a = "+ b);

    }
}
